x = 1
## over Multiple lines...
if x == 1 then	# with 'then'
	puts( 'ok' )
end

if x == 1		# without 'then'
	puts( 'ok' )
end
	
if x == 1 :		# with colon
	puts( 'ok' )
end

## On a single line...
if x == 1 then puts( 'ok' ) end	# with 'then'

if x == 1 : puts( 'ok' ) end		# with colon

